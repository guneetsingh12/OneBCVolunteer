// Helper function to lookup riding from postal code
export function lookupRidingFromPostalCode(postalCode: string): { riding: string; confidence: 'high' | 'medium' | 'low' | 'none' } {
  const prefix = postalCode.toUpperCase().replace(/\s/g, '').substring(0, 3);
  const riding = postalCodeToRiding[prefix];
  
  if (riding) {
    return { riding, confidence: 'high' };
  }
  
  // Check if it's a BC postal code (starts with V)
  if (prefix.startsWith('V')) {
    return { riding: 'Unknown - Needs Review', confidence: 'low' };
  }
  
  return { riding: 'Outside BC', confidence: 'none' };
}